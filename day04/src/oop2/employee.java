package oop2;

public class employee {


		public String id ;
		public String name;
		public int salary;
		
		public employee() {
		}
		
		
		
		public employee(String id, String name, int salary) {
			this.id = id;
			this.name = name;
			this.salary = salary;
		}
		
		public employee(String name, int salary) {
			this.name = name;
			this.salary = salary;
		}
		
	}

